package programa;

public class Whilezinho {

    public static void main(String[] args) {
        
        int i = 10;

        System.out.println("Contagem com while em -10...");

        while (i >= -10) {
            System.out.println(i + "...");
            --i;
        } 

        System.out.println("FIRE!");
    }
    
}
